#ifndef ETHER_PHY
#define ETHER_PHY

#include "if_rx62nreg.h"

typedef enum _phy_mode_t{
	PHY_MODE_10BASE_HALF	= 0x00,
	PHY_MODE_10BASE_FULL	= 0x01,
	PHY_MODE_100BASE_HALF	= 0x02,
	PHY_MODE_100BASE_FULL	= 0x03,
} PHY_MODE_T;

typedef enum _phy_state_t{
	PHY_STATE_UNINIT,
	PHY_STATE_RESETING,
	PHY_STATE_RESET,
	PHY_STATE_LINKE_WAIT,
	PHY_STATE_LINKED,
	PHY_STATE_NEGOTIATED,
} PHY_STATE_T;

extern PHY_STATE_T phy_reset(PHY_STATE_T state, uint8_t phy_addr);
extern PHY_STATE_T phy_initialize(PHY_STATE_T state, uint8_t phy_addr, PHY_MODE_T *mode);
extern bool_t phy_is_link(uint8_t phy_addr);
extern uint16_t phy_read_reg(uint8_t phy_addr, uint8_t rReg);
extern void phy_write_reg(uint8_t phy_addr, uint8_t wReg, uint16_t wdata);

#endif
